    public boolean checkAllFieldsFilledorNot() {
        if (txt_doctor_name.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Please Enter Doctor Name");
            txt_doctor_name.requestFocus();
            return false;
        }

        if (txt_doctor_mobile.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Please Enter Doctor Mobile Number");
            txt_doctor_mobile.requestFocus();
            return false;
        }

        if (txt_doctor_address.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Please Enter Doctor Address");
            txt_doctor_address.requestFocus();
            return false;
        }

        if (txt_doctor_username.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Please Enter Username");
            txt_doctor_username.requestFocus();
            return false;
        }

        if (txt_password.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Please Enter Password");
            txt_password.requestFocus();
            return false;
        }

        if (txt_password1.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Please Enter Re-enter Password");
            txt_password.requestFocus();
            return false;
        }

        if (!txt_password.getText().equals(txt_password1.getText())) {
            JOptionPane.showMessageDialog(null, "Password Does Not Match");
            txt_password.setText("");
            txt_password1.setText("");
            txt_password.requestFocus();
            return false;
        }
        return true;
    }

