    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        if (checkAllFieldsFilledorNot()) {
            try {

                Connection con = ConnectionDB.getConnection();
                if (con != null) {
                    String sql = "insert into doctor(name,mobile,address,username,password)values(?,?,?,?,?)";
                    PreparedStatement ps = con.prepareStatement(sql);

                    ps.setString(1, txt_doctor_name.getText().trim());
                    ps.setString(2, txt_doctor_mobile.getText().trim());
                    ps.setString(3, txt_doctor_address.getText().trim());
                    ps.setString(4, txt_doctor_username.getText().trim());
                    ps.setString(5, txt_password.getText());
                    if (ps.executeUpdate() == 1) {
                        JOptionPane.showMessageDialog(null, "Doctor Added Succefully");
                        ps.close();
                        con.close();
                        clearAllCompopent();
                        table_load();
                    } else {
                        JOptionPane.showMessageDialog(null, "Doctor Added FaileD");
                        ps.close();
                        con.close();

                    }

                }

            } catch (Exception e) {
                JOptionPane.showConfirmDialog(null, "Doctor Added Failed as: " + e.toString());
            }
        }


    }//GEN-LAST:event_jButton1ActionPerformed

